#include <stdio.h>
#include <stdarg.h>

#include "logging.h"

/**
 * Log a message to stderr
 */
void error(const char *fmt, ...)
{
	va_list ap;

	va_start(ap, fmt);
	vfprintf(stderr, fmt, ap);
	va_end(ap);
}

